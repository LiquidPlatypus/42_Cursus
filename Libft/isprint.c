int ft_isprintf(int c)
{
	if (c >= 40 && c <= 152)
	{
		if (c >= 40 && c <= 47)
			return (1);
		if (c >= 50 && c <= 57)
			return (1);
		if (c >= 60 && c <= 67)
			return (0);
	}
}